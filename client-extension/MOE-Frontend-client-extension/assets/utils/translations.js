// Minimal translation utilities used by the MOE frontend
// Provides hooks and helpers consumed by driver components

import Liferay from './Liferay';
import { useMemo } from 'react';

const DASH_LOCALE_TO_UNDERSCORE = {
  'en-US': 'en_US',
  'ar-SA': 'ar_SA',
};

const UNDERSCORE_LOCALE_TO_DASH = {
  en_US: 'en-US',
  ar_SA: 'ar-SA',
};

function normalizeToDash(locale) {
  if (!locale) return 'en-US';
  const lc = String(locale);
  if (UNDERSCORE_LOCALE_TO_DASH[lc]) return UNDERSCORE_LOCALE_TO_DASH[lc];
  return lc.replace('_', '-');
}

function normalizeToUnderscore(locale) {
  if (!locale) return 'en_US';
  const lc = String(locale);
  if (DASH_LOCALE_TO_UNDERSCORE[lc]) return DASH_LOCALE_TO_UNDERSCORE[lc];
  return lc.replace('-', '_');
}

export function getCurrentLanguage() {
  try {
    // Liferay exposes languageId like en_US
    const liferayLangId = Liferay?.ThemeDisplay?.getLanguageId?.();
    if (liferayLangId) {
      return normalizeToDash(liferayLangId);
    }
  } catch (_e) {}
  // Fallback to browser
  const navLang = (typeof navigator !== 'undefined' && (navigator.language || (navigator.languages && navigator.languages[0]))) || 'en-US';
  return normalizeToDash(navLang);
}

export function useTranslation() {
  const currentLanguage = getCurrentLanguage();
  const direction = currentLanguage.startsWith('ar') ? 'rtl' : 'ltr';

  const t = useMemo(() => {
    return (key) => {
      // First try to get from our translations object
      const languageTranslations = translations[currentLanguage];
      if (languageTranslations && languageTranslations[key]) {
        return languageTranslations[key];
      }
      
      // Fallback to Liferay Language API if available
      try {
        const translated = Liferay?.Language?.get?.(key);
        return translated || key;
      } catch (_e) {
        return key;
      }
    };
  }, [currentLanguage]);

  return { t, currentLanguage, direction };
}

export function getLocalizedHeaders(currentLanguage) {
  const lang = currentLanguage || getCurrentLanguage();
  return { 'Accept-Language': lang };
}

export function stripHtmlTags(value) {
  if (typeof value !== 'string') return value || '';
  return value.replace(/<[^>]*>/g, '');
}

export function getLocalizedValue(record, fieldName, currentLanguage) {
  if (!record || !fieldName) return '';
  const langDash = currentLanguage || getCurrentLanguage();
  const langUnderscore = normalizeToUnderscore(langDash);

  // Try typical *_i18n structure
  const i18nField = record[`${fieldName}_i18n`];
  if (i18nField && typeof i18nField === 'object') {
    // Support both dash and underscore keys just in case
    return (
      i18nField[langUnderscore] || i18nField[langDash] || i18nField['en_US'] || i18nField['en-US'] || ''
    );
  }

  // Try nested localized maps found in some headless endpoints
  const value = record[fieldName];
  if (value && typeof value === 'object') {
    return value[langUnderscore] || value[langDash] || value['en_US'] || value['en-US'] || '';
  }

  // Fallback to plain field
  return value || '';
}

export function getLocalizedStatus(status, currentLanguage) {
  if (!status) return '';
  // Provide a minimal mapping; customize as needed
  const key = String(status).toLowerCase();
  const t = (k) => {
    try { return Liferay?.Language?.get?.(k) || k; } catch (_e) { return k; }
  };
  switch (key) {
    case 'approved':
      return t('approved');
    case 'draft':
      return t('draft');
    case 'pending':
      return t('pending');
    case 'denied':
    case 'rejected':
      return t('rejected');
    default:
      return status;
  }
}

